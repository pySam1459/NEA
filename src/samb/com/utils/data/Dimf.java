package samb.com.utils.data;

public class Dimf {
	// This class represents a Dimension object which can handle real values
	
	public double width, height;
	
	public Dimf() {
		this.width = 0.0;
		this.height = 0.0;
	}
	
	public Dimf(double w, double h) {
		this.width = w;
		this.height = h;
		
	}

}
